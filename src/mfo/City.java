package mfo;

/**
 *
 * @author thang
 */
 public class City {
	// each object of this class will store coordinates of one city

	private double x;
	private double y;

	public City() {
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
}

